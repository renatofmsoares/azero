# Operadores de Comparacao

n1 = 10
n2 = 10

# Toda comparação no Ruby retorna verdadeiro ou false
# Tipo de resultado? Boleano

# Maior que
resultado = n1 > n2
puts resultado

# Menor que
resultado = n1 < n2
puts resultado

# Maior ou igual
resultado = n1 >= n2
puts resultado

# Diferente
n2 = '10'
resultado = n1 != n2
puts resultado # => true

# Igual
puts n1 == n2
puts n1.eql?(n2)
